      <h6>Rent is a Trap</h6>
      <p>Exploring the intersections of policing, displacement, and community resistance.</p>
    </div>
  
    <div class="landing-section">
      <div class="text-block">
        <p>Many people know Breonna Taylor’s name and the injustice of her murder by police. What’s less widely known is the role that real estate interests played in her death. According to Brenden Beck (2020), the police raid that killed Breonna was not an isolated event – it was part of a broader effort to displace residents from a neighborhood targeted for redevelopment:</p>
  
        <p class="quote">
          “The raid that killed Taylor was part of a broader effort to evict residents who were impeding the city’s Vision Russell redevelopment initiative. When they broke down Taylor’s door, the Louisville police were seeking to apprehend an associate of Jamarcus Glover, a former boyfriend of Taylor’s who lived on Elliott Avenue at the center of the city’s neighborhood 'renewal' project.”
        </p>
  
        <p>Her story reminds us that policing and property development are deeply connected forces. They determine who gets to stay, and who is surveilled, displaced, evicted, and subject to state violence.</p>
  
        <p>In Boston and across the country, this connection has only deepened since the 2008 financial crisis. Corporate homeownership of homes has soared, while private equity firms like Blackstone have made billions. Affording housing has become increasingly precarious for most Americans.</p>
  
        <p>This project looks at publicly available data—specifically non-criminal policing data like 311 noise complaints and building & property violations—to help illuminate the ways policing and real estate development work together at the city level.</p>
  
        <p>Our audience is community members like you: renters, homeowners, and neighbors who deserve transparency about the forces reshaping the city. By better understanding these patterns, we can build stronger movements to defend our homes, neighborhoods, and shared futures.</p>
      </div>
